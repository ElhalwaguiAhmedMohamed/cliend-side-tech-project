class Employee {
  constructor(
    _fname,
    _lname,
    _email,
    _age,
    _address,
    _username,
    _password,
    _type,
    _gender
  ) {
    this.Fname = _fname;
    this.Lname = _lname;
    this.Email = _email;
    this.Age = _age;
    this.Address = _address;
    this.UserName = _username;
    this.Password = _password;
    this.Type = _type;
    this.Gender = _gender;
  }
}

export { Employee };
